</div>
<p class="cutline" style="width:92%;">A trailer, broken bicycle and bright blue house sit in the historically Black neighborhood of Hopeville in Buckeye, Ariz. Feb. 25, 2023.</p>
</div>

<div class="body">
    <p>
        Wind whips through serene, empty streets around a mix of refurbished homes, stationary trailers and neglected abodes. Trash and discarded clothes cover some yards, while others are perfectly manicured. 
    </p>
    <p>
        The past and future meet here in Allenville, a Black community built on the outskirts of Buckeye, Arizona. The ark may have come for Noah in the Old Testament, but it didn’t quite make it to Allenville. For decades, this desert town borne of flood plain disasters has proven a case study on racial distrust – or more specifically on how poor Black communities become casualties of their white neighbors’ agendas. 
    </p>
</div>

<div class="photo">
    <img src="https://github.com/cdemon12/capstone/raw/main/src/routes/photo-essay/photos/Allenville_Photo1.JPG" alt="">
    <p class="cutline">An empty drum lies on its side in the midst of what used to be the thriving, Black neighborhood of Allenville in Buckeye, Ariz. Feb. 25, 2023. The refuse left behind is symbolic of the rapid decline of the neighborhood.</p>
</div>

<div class="body">
    <p>
        When 24-year-old Black geologist Daniel Robinson went mysteriously missing in Buckeye, Arizona, a so-called ‘sundown town’ near Phoenix , Black residents were forced to weigh Buckeye’s public statements with the painful lessons of history a few streets over.  
    </p>
    <p>
        Population zero, Allenville sits on the Gila River Plains, an area prone to extraordinary flooding. After the 1978 flood, the federal government paid for the citizens of Allenville to relocate from the Gila River’s floodplain, to the other side of Buckeye. 
    </p>
</div>

<div class="photo">
    <img src="https://github.com/cdemon12/capstone/raw/main/src/routes/photo-essay/photos/Allenville_Puddle_Photo2.JPG" alt="">
    <p class="cutline">Water pools in a divet on the land where Allenville formerly sat.</p>
</div>

<div class="body">
    <p>
        It was here, on this plot of land just off of South Palo Verde Road, that the community of Hopeville was established in the 1980s.
    </p>
    <p>
        Since then, Hopeville has sprouted into a community.  On Sundays, the church holds a service led by Bishop Abraham Harris III for Black congregants. On many days, Hopeville feels as blessed as its name.
    </p>
</div>

<div class="photo">
    <img src="https://github.com/cdemon12/capstone/raw/main/src/routes/photo-essay/photos/Hopeville_Kids_Photo.JPG" alt="">
    <p class="cutline">Children play in the yard of a house in Hopeville, the neighborhood established by the Army Corps of Engineers for displaced Allenville residents in the early 1980s. </p>
</div>

<div class="body">
    <p>
        Yet after barely four decades of treading water, Hopeville and its resilient residents now worry their town could be washed away yet again — not by flooding this time around, but by its own growth-crazed local officials.
    </p>
    <h3>
        The Before
    </h3>

    <p>
        The Allenville origin story opens with a scam. Back in 1944 Lee Norton, a local white realtor, sold a plot of land near Buckeye to Black people needing work and housing. Trouble was, the land sat smack in a floodplain, which meant anything built or cultivated on those plots could get washed away in a storm. But back then, prior to the passage of the 1968 Fair Housing Act, Black people were forbidden from living in Buckeye proper, according to local archives.
    </p>
    <p>
        So John Allen, a Black farmer, purchased the land from Norton for $1.
    </p>
    <h3>
        The Rise and Fall of Allenville
    </h3>
    <p>
        From the moment Allenville hung its first shingle, an irony plagued residents: despite living in a flood zone and enduring flood after flood, the drinking water was not potable.
    </p>
</div>

<div class="photo">
    <img src="https://github.com/cdemon12/capstone/raw/main/src/routes/photo-essay/photos/Tree_Allenville_Photo4.JPG" alt="">
    <p class="cutline">A Joshua-esque tree stands tall against the bright blue sky in Allenville Feb. 25, 2023. It is a striking presence and remarkable that it has stood the test of time when an entire neighborhood of people were washed away.</p>
</div>
<div class="body">
    <p>
        Now and then, floodwaters would overflow and cause sewage and waste generated from then all-white Buckeye to spill over onto Allenville’s streets and become a breeding ground for mosquitoes. Such unsanitary conditions forced Allenville residents to source their water from Buckeye, which meant frequent trips to gas stations to fill up jugs and collect their 5-gallon ration.
    </p>
    <p>
        Despite tales of such hardship, longtime residents still boast about the  sense of community Allentown enjoyed back then - at least until 1978 when a couple of big floods nearly swept through town. Damage from the floods crippled the community, which had swelled with 400 Black working class people, destroying homes and farms and forever changing Allenville’s landscape.
    </p>
    <p>
        “Quote here”
    </p>
    <p>
        The destruction from the floods created several schisms in the community: some residents stayed in government-provided trailers in and around Buckeye, others moved away and still others stayed put and attempted to rebuild.
    </p>
</div>
<div class="photo">
    <img src="https://github.com/cdemon12/capstone/raw/main/src/routes/photo-essay/photos/Building_Allenville_Photo_5.JPG" alt="">
    <p class="cutline">What’s left of a building sits dilapidated on the property of the former neighborhood of Allenville Feb. 25, 2023.</p>
</div>
<div class="body">
    <p>
        Yet again, local policies were not in residents’ favor: homes had to be rebuilt in accordance with particular county codes and regulations, and many of the economically disadvantaged Allenville residents could not afford to rebuild their homes up to code. Many residents applied for grants to reconstruct their homes in compliance with the regulations and were denied due to TK.  
    </p>
    <p>
        Eventually, the federal government sponsored a multimillion-dollar relocation project to move the remaining Allenville residents north of Buckeye, out of the path of the rivers’ wrath. The government enlisted the help of the Army Corps of Engineers to construct the community that would come to be known as Hopeville.
    </p>
    <h3>
        The Present – Hopeville
    </h3>
</div>
<div class="photo">
    <img src="https://github.com/cdemon12/capstone/raw/main/src/routes/photo-essay/photos/Hopeville_Photo_6.JPG" alt="">
    <p class="cutline">The neighborhood of Hopeville is framed by mountains in Buckeye, Ariz. Feb. 25, 2023.
    </p>
</div>
<div class="body">
    <p>Brice Caldwell, known as ‘Buddy’, has lived in Hopeville since 2002, and his grandfather lived in Hopeville before him. Caldwell’s grandfather was actually one of the town’s original residents,</p>
    <p>Caldwell says he has witnessed growing numbers of Hispanic and white families moving to Allenville since his arrival after the last big flood in 1978. The result, he says, is that the Black community has felt less cohesive ever since. </p>
</div>
<div class="photo">
    <img src="https://github.com/cdemon12/capstone/raw/main/src/routes/photo-essay/photos/Brice_Photo_7.JPG" alt="">
    <p class="cutline">Brice Caldwell works on his truck in the driveway of the Hopeville home he has inhabited for more than 20 years Feb. 25, 2023.
    </p>
</div>
<div class="body">
    <p>Even more, as elders pass away, younger Black residents show little interest in the preserving the tight-knit culture created by previous generations. “It's a lot of lost history and things that's gonna be lost,” Caldwell says. “Society makes us not want to learn. It makes us want to just always go, go, go, go … [and] never sit back [and] enjoy what we have in front of us.”</p>
    <p>Chancey Wages moved to Hopeville from Montana and, like Caldwell, aims to eventually move onto a larger plot of land. For now, Wages, a white resident, doesn’t mind living in Hopeville.</p>
</div>
<div class="photo">
      <img src="https://github.com/cdemon12/capstone/raw/main/src/routes/photo-essay/photos/Chancey_Photo_8.JPG" alt="">
        <p class="cutline">Montana transplant Chancey Wages looks back at his front door expectantly for his wife Feb. 25, 2023. They are celebrating their first wedding anniversary in Hopeville.</p>
    </div>
<div class="body">
    <p>“As far as being in a neighborhood, I'm not used to having neighbors, but it's not bad at all compared to some places I've seen,” Wages says. “Neighbors are pretty polite, we get along pretty well, we're pretty friendly with each other … watch out for each other. ”</p>
    <p>Wages is aware the town used to be predominantly Black, and he says some residents wish that was still the case. Wages himself had never seen a Black person until he was 16 when a Black man came into the grocery store in which he was working, but despite having little exposure to diversity before moving to Hopeville, living in a community with such a strong Black history and several Black residents does not phase him.</p>
    <p>“It doesn't have to be, you know, a separation or anything like that,” Wages says. “I mean, if you slice your arm open, what color is your blood? I don't care what somebody looks like or anything … What I care about is what their character is. I grew up ranching [and] you judge them how they can ride a horse, not what color their skin is.”</p>
</div>
<div class="photo">
    <img src="https://github.com/cdemon12/capstone/raw/main/src/routes/photo-essay/photos/Hopeville_Photo_9.JPG" alt="">
    <p class="cutline">Clouds blanket the sky over the neighborhood of Hopeville Feb. 25, 2023. On one side of a cul-de-sac is what appears to be a newly renovated house, and on another sits a trailer with a roll cart full of discarded clothing.</p>
</div>
<div class="body">
    <p>Melvin Douglass, a Black former trucker and Phoenix transplant, lives by a similar creed.</p>
    <p>“I’m surrounded by some of everybody and as long as everybody speak to everybody, it don’t matter to me,” Douglass says. </p>
    <p>Caldwell hopes to start his farm; Wages dreams of living on an expanded ranch with no interference from the government in his day-to-day affairs; and Douglass, though he moved to Hopeville only six years ago, emphasizes lots of space is needed for his multiple trailers.</p>
    <p>Everyone wants more space for themselves and their own endeavors, and as they expand, Hopeville may collapse.</p>
    <h3>Hopeville’s Future</h3>
</div>
<div class="photo">
    <img src="https://github.com/cdemon12/capstone/raw/main/src/routes/photo-essay/photos/Hopeville_Photo_10.JPG" alt="">
    <p class="cutline">On the other side of South Palo Verde Road, farther down the way, is the Buckeye Municipal Airport. This expanse of land just across the way from Hopeville will be consumed by a commercial center to be built in the next couple of decades.</p>
</div>
<div class="body">
    <p>Just south of Hopeville, on the opposite side of South Palo Verde Road, is Buckeye Municipal Airport. The airport, which has been under the ownership of Buckeye since the 1960s, serves about 250 flights per day, and local officials do not know how much that number will increase over the next decade.</p>
    <p>Rhonda Douglass, who has been married to Melvin for TK years, says she received the city’s letter about the airport’s expansion in TK (season) 20TK. </p>
    <p>Douglass also says that based on government notices, and her own digging, she has concluded that all of Hopeville is slated to become airport property, pushing Hopeville residents out of their homes in the next 20-30 years, she says.</p>
    <p>Buckeye TK declined to comment on details of future airport expansion, except that residents were notified about the expansion in May 2022. “Quote”</p>
    <p>According to the city’s Imagine Buckeye 2040 General Plan, Buck plans to create “mixed use areas that provide live, work, and play destination locations” in the area. This so-called Airport Area Plan shows an activity center that covers the entire neighborhood of Hopeville.</p>
    <p>The area “will be used for policy guidance for the City staff and the development community to guide growth and identify future priorities for the activity center,” according to a fact sheet on the city’s website. “This will be accomplished by understanding existing
        conditions, planned infrastructure, and the vision for the area.</p>
    <p> “We're gonna wait,” Rhonda Douglass says. “And … I'm 73 years old. He's 76. So we don't know where we’re gonna be in 20, 30 years.”</p>
    <p>Buckeye spokesperson Annie DeChance says the goal of the Airport Area Plan is “to ensure Hopeville residents have minimal impacts as the airport grows with the city.”</p>
    <p> Meanwhile, Hopeville residents are left wondering what constitutes a “minimal impact”? Should a new, higher-rent development move into Hopeville, there are concerns current residents may not be able to afford to live in the area.</p>
    <p>“We are involving them in the process for each of these projects so they are fully knowledgeable about the potential future growth at the airport,” DeChance said in an email, adding that Hopeville residents should speak their mind at public hearings, city council workshops, planning and zoning workshops and community meetings.</p>
    <p>Rhonda Douglass views the Airport Area Plan to level Hopeville in favor of a newer development as part of the area’s continuing legacy of prejudice against people of color – whether it's relocating them onto a flood plain or pushing them out to make way for developers.</p>
    <p>Like most of Hopeville’s residents, she’s angry and hurt that Buckeye would force another relocation – but parses her words in expressing it. “There are some good people, I mean, that’s anywhere,” she says, “And there is this obvious and blatant racism, that’s not just Buckeye.”</p>
